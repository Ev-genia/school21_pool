int	ft_any(char **tab, int(*f)(char*))
{
	int	rez;
	int	i;

	rez = 0;
	i = 0;
	while (tab[i] != 0)
	{
		rez = (*f)(tab[i]);
		if (rez != 0)
			return (1);
		i++;
	}
	return (0);
}
